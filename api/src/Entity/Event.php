    // Koordinátor - může být interní (z personálu) nebo externí
    #[ORM\Column(type: Types::INTEGER, nullable: true)]
    private ?int $coordinatorId = null; // ID z tabulky staff_member (interní)

    #[ORM\Column(type: Types::BOOLEAN, options: ['default' => false])]
    private bool $isExternalCoordinator = false;

    #[ORM\Column(type: Types::STRING, length: 255, nullable: true)]
    private ?string $externalCoordinatorName = null;

    #[ORM\Column(type: Types::STRING, length: 255, nullable: true)]
    private ?string $externalCoordinatorEmail = null;

    #[ORM\Column(type: Types::STRING, length: 50, nullable: true)]
    private ?string $externalCoordinatorPhone = null;

    #[ORM\Column(type: Types::TEXT, nullable: true)]
