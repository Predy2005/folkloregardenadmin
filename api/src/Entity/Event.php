    /** @return Collection<int, EventGuest> */
    public function getGuests(): Collection
    {
        return $this->guests;
    }

    public function addGuest(EventGuest $g): self
    {
        if (!$this->guests->contains($g)) {
            $this->guests->add($g);
            $g->setEvent($this);
        }
        return $this;
    }

    public function removeGuest(EventGuest $g): self
    {
        if ($this->guests->removeElement($g)) {
            if ($g->getEvent() === $this) {
                $g->setEvent(null);
            }
        }
        return $this;
    }

