    #[ORM\Column(type: Types::STRING, length: 20, nullable: true)]
    private ?string $invoiceDic = null;

    #[ORM\Column(type: Types::TEXT, nullable: true)]
    private ?string $invoiceAddress = null;

    // Platba
    #[ORM\Column(type: Types::DECIMAL, precision: 15, scale: 2, nullable: true)]
    private ?string $totalPrice = null;

    #[ORM\Column(type: Types::DECIMAL, precision: 15, scale: 2, nullable: true)]
    private ?string $depositAmount = null;

    #[ORM\Column(type: Types::BOOLEAN, options: ['default' => false])]
    private bool $depositPaid = false;

    #[ORM\Column(type: Types::STRING, length: 50, nullable: true)]
    private ?string $paymentMethod = null; // 'CASH', 'BANK_TRANSFER', 'CARD', 'INVOICE'

    // Status
    #[ORM\Column(type: Types::STRING, length: 50, options: ['default' => 'PLANNED'])]
