    #[ORM\Column(type: Types::DATETIME_MUTABLE)]
    private \DateTimeInterface $createdAt;

    #[ORM\Column(type: Types::DATETIME_MUTABLE)]
    private \DateTimeInterface $updatedAt;

    // Relations to child tables
    /** @var Collection<int, EventGuest> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventGuest::class, cascade: ['persist', 'remove'])]
    private Collection $guests;

    /** @var Collection<int, EventMenu> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventMenu::class, cascade: ['persist', 'remove'])]
    private Collection $menus;

    /** @var Collection<int, EventBeverage> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventBeverage::class, cascade: ['persist', 'remove'])]
    private Collection $beverages;

    /** @var Collection<int, EventStaffAssignment> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventStaffAssignment::class, cascade: ['persist', 'remove'])]
    private Collection $staffAssignments;

    /** @var Collection<int, EventSchedule> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventSchedule::class, cascade: ['persist', 'remove'])]
    private Collection $schedules;

    /** @var Collection<int, EventVoucher> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventVoucher::class, cascade: ['persist', 'remove'])]
    private Collection $vouchers;

    /** @var Collection<int, EventTable> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventTable::class, cascade: ['persist', 'remove'])]
    private Collection $tables;

    /** @var Collection<int, EventSpace> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventSpace::class, cascade: ['persist', 'remove'])]
    private Collection $spaces;

    /** @var Collection<int, EventInvoice> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventInvoice::class, cascade: ['persist', 'remove'])]
    private Collection $eventInvoices;

    /** @var Collection<int, FloorPlanElement> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: FloorPlanElement::class, cascade: ['persist', 'remove'])]
    private Collection $floorPlanElements;

    /** @var Collection<int, EventTransport> */
    #[ORM\OneToMany(mappedBy: 'event', targetEntity: EventTransport::class, cascade: ['persist', 'remove'])]
    private Collection $transportAssignments;

    public function __construct()
    {
        $this->guests = new ArrayCollection();
        $this->menus = new ArrayCollection();
        $this->beverages = new ArrayCollection();
        $this->staffAssignments = new ArrayCollection();
        $this->schedules = new ArrayCollection();
        $this->vouchers = new ArrayCollection();
        $this->tables = new ArrayCollection();
        $this->spaces = new ArrayCollection();
        $this->eventInvoices = new ArrayCollection();
        $this->floorPlanElements = new ArrayCollection();
        $this->transportAssignments = new ArrayCollection();
        $this->createdAt = new \DateTime();
        $this->updatedAt = new \DateTime();
    }

    #[ORM\PrePersist]
    public function onPrePersist(): void
    {
        $now = new \DateTime();
        $this->createdAt = $this->createdAt ?? $now;
        $this->updatedAt = $now;
        $this->recalcGuestsTotal();
    }

    #[ORM\PreUpdate]
    public function onPreUpdate(): void
    {
        $this->updatedAt = new \DateTime();
        $this->recalcGuestsTotal();
    }

    private function recalcGuestsTotal(): void
    {
        $this->guestsTotal = (int)($this->guestsPaid ?? 0) + (int)($this->guestsFree ?? 0);
    }

    // Getters/setters (subset for required operations)
